                            <b>◎特码：</b><br>
                            开奖球数共七个(包括特码)，开出的最后一个球为特码。投注1~49任意一个号码，并以即时赔率作为派奖依据，买中即获奖。<br>
                            <br>
                            <b>◎特码单双：</b><br>
                            特码为双数叫特双，如 08、16；为单数叫特单， 如 21、35；（特码开49号，则视为和）。<br>
                            <br>
                            <b>◎特码大小：</b><br>
                            开出之特码范围在（1-24）为特码小， 范围在（25-48）为特码大； （特码开49号，则视为和）。 <br>
                            <br>
                            <b>◎特码合数单双：</b><br>
                            以特码个位和十位数字之和来判断，如 01、12、32 为合单，02、11、33 为合双；（特码开49号，则视为和）。 <br>
                            <br>
                            <b>◎特码合数大小：</b><br>
                            以特码个位数和十位数之和来决定大小，合数大于或等于7为大，小于或等于6为小；（特码开49号，则视为和）。 <br>
                            <br>
                            <b>◎特码尾数大小：</b><br>
                            以特码尾数若0尾~4尾为小、5尾~9尾为大；如01、32、44为特尾小；如05、18、19为特尾大（特码开49号，则视为和）。<br>
                            
                            <br>
                            <b>◎正码：</b><br>
                            当期开出前六个号码叫正码。下注号码如在六个正码号码里为中奖。<br>
                            <br>
                            <b>◎总分单双：</b>(请在“正码”页面中投注)<br>
                            所有七个开奖号码的分数总和是单数叫(总分单)，如分数总和是115、183；分数总和是双数叫(总分双)，如分数总和是108、162。假如投注组合符合中奖结果，视为中奖，其余情形视为不中奖。（此玩法无和局的情况）。<br>
                            <br>
                            <b>◎总分大小：</b>(请在“正码”页面中投注)<br>
                            所有七个开奖号码的分数总和大于或等于175为总分大；分数总和小于或等于174为总分小。
                            如开奖号码为02、08、17、28、39、46、25，分数总和是165，则总分小。假如投注组合符合中奖结果，视为中奖，其余情形视为不中奖。（此玩法无和局的情况）。<br>
                            
                            <br>
                            <b>◎正特：</b><br>
                            指正1特~正6特，下注的正特码与开出正码开奖顺序及开奖号码相同，视为中奖，如第一个正码开奖为18号，下注正1码特为18则视为中奖，其它情况视为不中。<br>
                            
                            <br>
                            <b>◎正码1-6：</b><br>
                            当期开出前六个号码叫正码。开出第一个的号码叫正码1，依次为正码2、正码3……正码6(并不以号码大小排序)。正码1、正码2、正码3、正码4、正码5、正码6的大小、单双、合数大小、合数单双和特码的规则相同，如正码1为29，则正码1为大，为单；正码2为8，则正码2为小，
                            为双；假如投注内容符合中奖结果，视为中奖，其余情形视为不中奖。（六个正码中如有号码为49，则单项为和） 。<br>
                            
