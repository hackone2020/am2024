                            <b>◎连码：二全中</b>（可选择多码复式或者胆拖或者对碰形式进行投注）<br>
                            所投注的每二个号码为一组合，二个号码都是开奖号码之正码，视为中奖，其余情形视为不中奖（含一个正码加一个特码之情形）。<br>
                            
                            <br>
                            <b> ◎连码：二中特</b>（可选择多码复式或者胆拖或者对碰形式进行投注）<br>
                            所投注的每二个号码为一组合，二个号码都是开奖号码之正码，叫二中特之中二；若其中一 个是正码，一个是特码，叫二中特之中特；其余情形视为不中奖。<br>
                            
                            <br>
                            <b>◎连码：特串</b>（可选择多码复式或者胆拖形式进行投注）<br>
                            所投注的每二个号码为一组合，其中一个是正码，一个是特码，视为中奖，其余情形视为不中奖（含二个号码都是正码之情形）。<br>
                            
