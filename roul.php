                            <b>◎连码：硬特</b>（可选择多码复式或者胆拖形式进行投注）<br>
                            所投注的每二个号码为一组合，其中第一个是特码，第二个是正码，则视为中奖，其余情形视为不中奖（含第一个是正码、第二个是特码之情形）。<br>
                            
