                            <b>◎连码：四全中</b>（可选择多码复式或者胆拖形式进行投注） <br>
                            所选投注的每四个号码为一个组合，若全部四个号码都是开奖号码之正码则视为中奖，其余视为不中奖。如06、07、08、09四个都是开奖号码之正码，视为中奖，如三个正码加上一个特码视为不中奖。<br>
                            
                            <br>
                            <b>◎连码：三全中</b>（可选择多码复式或者胆拖形式进行投注） <br>
                            所投注的每三个号码为一组合，若三个号码都是开奖号码之正码，视为中奖，其余情形视为不中奖。如06、07、08三个都是开奖号码之正码，视为中奖，如两个正码加上一个特码视为不中奖。<br>
                            
                            <br>
                            <b>◎连码：三中二</b>（可选择多码复式或者胆拖形式进行投注）<br>
                            所投注的每三个号码为一组合，若其中2个是开奖号码中的正码，即为三中二，视为中奖；若3个都是开奖号码中的正码，即为三中二之中三，其余情形视为不中奖，如06、07、08
                            为一组合，开奖号码中有06、07两个正码，没有08，即为三中二，按三中二赔付；如开奖
                            号码中有06、07、08三个正码，即为三中二之中三，按中三赔付；如出现1个或没有，视为不中奖。<br>
                            
